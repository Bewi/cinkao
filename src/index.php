            <div class="timeline-block">
                <div class="timeline-content">
                    <p>
                        2014, Thomas Santagata à la guitare (Alema/IRIS) et JC Van Hecke Basse (Gil Breac/Alema) fondent le groupe qui s'appelle aujourd'hui Cinkao.
                    </p>
                    <p>
                        Rejoins rapidement par Frank Venmans au clavier, Alain Jonet à la batterie et Pascal de Maeseneire au chant.
                    </p>
                    <p>
                        Benjamin Wilfart reprendra la batterie en 2014 et Nicolas Corder le chant en 2015 pour aboutir à la configuration actuelle
                    </p>
                    <p>
                        Le projet de Cinkao est de composer et produire un rock en français musicalement inspiré d'univers
                        aussi différents que les groupes rock anglo-saxon, la chanson française et même quelques touches
                        de jazz                
                    </p>
